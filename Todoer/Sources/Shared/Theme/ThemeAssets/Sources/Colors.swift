import SwiftUI

/// Extension providing themed colors for the application
public extension Color {
    // MARK: - Colors
    
    /// Black background color
    static let backgroundBlack = Color("backgroundBlack", bundle: .module)
    /// White background color
    static let backgroundWhite = Color("backgroundWhite", bundle: .module)
    /// Black border color
    static let borderBlack = Color("borderBlack", bundle: .module)
    /// Primary border color
    static let borderPrimary = Color("borderPrimary", bundle: .module)
    /// Black button color
    static let buttonBlack = Color("buttonBlack", bundle: .module)
    /// Destructive action button color
    static let buttonDestructive = Color("buttonDestructive", bundle: .module)
    /// Secondary button color
    static let buttonSecondary = Color("buttonSecondary", bundle: .module)
    /// White button color
    static let buttonWhite = Color("buttonWhite", bundle: .module)
    /// Black text color
    static let textBlack = Color("textBlack", bundle: .module)
    /// Secondary text color
    static let textSecondary = Color("textSecondary", bundle: .module)
    /// White text color
    static let textWhite = Color("textWhite", bundle: .module)
    /// Secondary background color
    static let backgroundSecondary = Color("backgroundSecondary", bundle: .module)
}
